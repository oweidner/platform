package database

import "database/sql"

// GenericDelete deletes an object from the database.
func GenericDelete(db *sql.DB, query string, params interface{}) error {

	stmt, err := db.Prepare(query)
	if err != nil {
		return err
	}
	_, err = stmt.Query(params)
	if err != nil {
		return err
	}

	return nil
}
